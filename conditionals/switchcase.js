//using switch case in js
const month=3
switch(month){
    case 1:
        console.log("the month is janauary")
        break;
    case 2:
        console.log("the month is feb")
        break;
    case 3:
        console.log("the month is march")
        break;
    default:
        console.log("the default text is executed")
        break;
}